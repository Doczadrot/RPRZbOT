"""
Этот модуль реализует функцию, которая запрашивает локальную языковую модель с целью получения ответов на вопросы.
Он использует библиотеку `loguru` для логирования процесса, отслеживая этапы выполнения
и результаты промежуточных вычислений.
Модуль сконфигурирован на работу с моделью обработки естественного языка и позволяет формулировать запросы
в форме промпта, который встраивается в конфигурацию модели для генерации ответов на заданные вопросы.
Основное предназначение этого модуля — выполнение упрощённой генерации текста на основе поступающего
от пользователя вопроса, используя заданную локальную модель.
"""

from loguru import logger

# Настройка логирования с использованием loguru
logger.add(
    "log/Simple_Request_Local_Model.log", # Путь к файлу логов
    format="{time} {level} {message}", # Формат записи: время, уровень, сообщение
    level="DEBUG", # Уровень логирования: DEBUG означает запись всех сообщений
    rotation="100 KB", # Файл логов будет архивироваться каждые 100 КБ
    compression="zip" # Архивация старых логов в ZIP-архив
)

def get_model_response(topic):
    """
    Функция для получения ответа от локальной языковой модели на заданную тему.

    Параметры:
    - topic (str): Вопрос или тема, на которую нужно получить ответ.

    Возвращает:
    - str: Сгенерированный языковой моделью ответ.
    """

    logger.debug('...get_model_response') # Запись в лог о начале работы функции

    # Загрузка модели для обработки языка (LLM)
    from langchain_ollama import ChatOllama
    logger.debug('LLM') # Запись в лог о загрузке LLM
    # Инициализация локальной языковой модели с параметрами
    local_llm = "qwen2.5vl:3b"
    llm = ChatOllama(model=local_llm, temperature=0) # Инициализация модели с температурой 0 (для более детерминированных ответов)


    # Промпт (инструкция для модели)
    rag_prompt = """Ты являешься русскоязычным помощником. 
    Внимательно изучи вопрос:
    {question}
    Дайте ответ на русском языке. 
    Используйте не более трех предложений.
    Ответ:"""

    # Формирование запроса для LLM
    from langchain_core.messages import HumanMessage
    rag_prompt_formatted = rag_prompt.format( question=topic) # Вставляем вопрос пользователя в промпт

    # Отправка запроса (сформированного промпта) в модель и получение ответа
    generation = llm.invoke([HumanMessage(content=rag_prompt_formatted)]) # Отправляем промпт модели и получаем объект ответа
    model_response = generation.content # Извлекаем текстовое содержимое ответа
    logger.debug(model_response) # Записываем ответ модели в лог
    return model_response # Возвращаем сгенерированный ответ

if __name__ == "__main__": # Этот блок выполняется только при прямом запуске файла
    # Задание темы для запроса к модели
    topic = 'Объясни понятие RAG (Retrieval-Augmented Generation).' # Вопрос пользователя
    logger.debug(topic) # Записываем вопрос в лог

    # Получение ответа от модели на заданную тему
    model_response = get_model_response(topic) # Вызываем функцию для получения ответа
    print("Ответ:", model_response) # Выводим ответ на консоль